def print_arithmetic_operation(x,y):#함수이름을 print_arithmetic_operation이고 매개변수를 (x,y)로 받겠다.
    print(x+y)                       #x+y를 실행해라.
    print(x-y)                       #x-y를 실행해라.
    print(x*y)                       #x*y를 실행해라.
    print(x/y)                       #x/y를 실행해라.
print_arithmetic_operation(1,2)      #인수 x=1 y=2를넣음.